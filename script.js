const langSelector = document.getElementById('lang-selector');

function updateContent(lang) {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            el.innerHTML = translations[lang][key];
        } else if (translations['en'][key]) {
            // Fallback to English if key missing in target lang
            el.innerHTML = translations['en'][key];
        }
    });
    // Update RTL for Arabic
    document.body.dir = lang === 'ar' ? 'rtl' : 'ltr';
}

langSelector.addEventListener('change', (e) => {
    updateContent(e.target.value);
});

// Initial load with Auto-Detection
function detectLanguage() {
    const browserLang = navigator.language.split('-')[0]; // e.g. "tr-TR" -> "tr"
    const supportedLangs = Object.keys(translations);
    
    if (supportedLangs.includes(browserLang)) {
        return browserLang;
    }
    return 'en'; // Default to English
}

const initialLang = detectLanguage();
langSelector.value = initialLang;
updateContent(initialLang);

// Header Scroll Effect
const header = document.getElementById('header');
window.addEventListener('scroll', () => {
    if (window.scrollY > 50) {
        header.classList.add('scrolled');
    } else {
        header.classList.remove('scrolled');
    }
});

// Mobile Menu (Basic implementation)
const mobileMenu = document.getElementById('mobile-menu');
const navLinks = document.querySelector('.nav-links');

if (mobileMenu) {
    mobileMenu.addEventListener('click', () => {
        navLinks.classList.toggle('active');
        // Optional: Animate hamburger to X
        mobileMenu.classList.toggle('is-active');
    });

    // Close menu when clicking a link
    document.querySelectorAll('.nav-links a').forEach(link => {
        link.addEventListener('click', () => {
            navLinks.classList.remove('active');
        });
    });
}

// Smooth Scroll for anchor links
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
        e.preventDefault();
        const targetId = this.getAttribute('href');
        const targetElement = document.querySelector(targetId);
        
        if (targetElement) {
            window.scrollTo({
                top: targetElement.offsetTop - 80,
                behavior: 'smooth'
            });
        }
    });
});

// Intersection Observer for animations
const observerOptions = {
    threshold: 0.1
};

const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.classList.add('fade-in');
        }
    });
}, observerOptions);

document.querySelectorAll('.feature-card, .pricing-card').forEach(el => {
    observer.observe(el);
});
